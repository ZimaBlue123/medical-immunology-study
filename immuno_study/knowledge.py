def get_module_by_tag(tag: str) -> str | None:
    """根据标签获取对应的模块ID"""
    tag_lower = tag.lower()
    for module_id, tags in MODULE_TAGS.items():
        if tag_lower in [t.lower() for t in tags]:
            return module_id
    return None


def get_module_knowledge(module_id: str) -> dict[str, Any] | None:
    """获取模块知识内容"""
    return KNOWLEDGE_BASE.get(module_id)


def get_all_modules() -> list[dict[str, Any]]:
    """获取所有模块的基本信息"""
    return [
        {"id": mid, "title": data["title"], "objectives": data["objectives"]}
        for mid, data in KNOWLEDGE_BASE.items()
    ]


def get_concept_by_term(term: str) -> dict[str, Any] | None:
    """根据术语搜索概念"""
    term_lower = term.lower()
    for module_id, module_data in KNOWLEDGE_BASE.items():
        for concept in module_data.get("key_concepts", []):
            if term_lower in concept["term"].lower():
                return {**concept, "module": module_data["title"], "module_id": module_id}
    return None


def search_concepts(query: str) -> list[dict[str, Any]]:
    """搜索所有匹配的概念"""
    query_lower = query.lower()
    results = []
    for module_id, module_data in KNOWLEDGE_BASE.items():
        for concept in module_data.get("key_concepts", []):
            if query_lower in concept["term"].lower() or query_lower in concept["definition"].lower():
                results.append({**concept, "module": module_data["title"], "module_id": module_id})
    return results


def get_confusion_tips(tag: str) -> list[dict[str, Any]]:
    """获取与标签相关的混淆点提示"""
    module_id = get_module_by_tag(tag)
    if not module_id:
        return []
    module_data = KNOWLEDGE_BASE.get(module_id, {})
    return module_data.get("confusions", [])


